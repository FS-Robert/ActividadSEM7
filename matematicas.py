import math

# Calcular la raíz cuadrada de 100
raiz = math.sqrt(100)
print("La raíz cuadrada de 100 es:", raiz)

# Calcular el coseno de 90 grados (convertido a radianes)
coseno = math.cos(math.radians(90))
print("El coseno de 90 grados es:", coseno)

# Obtener el valor de pi
print("El valor de pi es:", math.pi)